var chanceVideo = document.getElementById("chanceVideo");

function playVideo() {
	chanceVideo.play();
}

function pauseVideo() {
	chanceVideo.pause();
}

function restartVideo() {
	chanceVideo.pause();
	chanceVideo.currentTime = 0;
	chanceVideo.load();
}


